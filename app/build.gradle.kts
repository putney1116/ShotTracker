// Google Maps API key is read from local.properties (git-ignored) so it is never
// committed. Falls back to an empty string if absent (e.g. CI without the key),
// which lets the build succeed though the map will not render.
val mapsApiKey: String = Properties().apply {
    val f = rootProject.file("local.properties")
    if (f.exists()) f.inputStream().use { load(it) }
}.getProperty("MAPS_API_KEY", "")

